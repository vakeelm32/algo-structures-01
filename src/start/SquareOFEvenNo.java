package start;

public class SquareOFEvenNo {

    // print square of even no from given array

    public static void main(String[] args) {

        int a[] = {2, 4, 1, 12, 9, 30, 4};



        for(int i=0;i<a.length;i++){

            if (a[i]%2==0){

                System.out.println("square="+ i*i);
            }
        }

    }







}
